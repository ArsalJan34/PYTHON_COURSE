age = 20

# This if statement checks the age
if age >= 18:
    # Everything indented here belongs to the if block
    print("You are an adult")

    # This line is STILL inside the if block
    print("You can vote")
else:
    # This block runs only if the if condition is False
    print("You are under 18")

# This line is OUTSIDE the if-else block
print("Program finished")
