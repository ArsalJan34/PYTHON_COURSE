# Mathematical Operators in python
print(1+2)
print(5-2)
print(2*2)
print(2/3)
# Here the division will print float value because python here does the implicit type casting
# so we use it to convert it to int
print(2//3)
# but we only use this when  we need a digit which has no decimal values
# and here is how we can get power of digits
print(2**2)
# Now this will print the power of 2 by 2 times
